        <h1 id="erp-tokominimarket-api-documentation">ERP Toko/Minimarket API Documentation</h1>
<h2 id="introduction">Introduction</h2>
<p>Selamat datang di dokumentasi API ERP Toko/Minimarket. Sistem ini dirancang untuk mengelola operasional toko retail dan minimarket dengan fitur lengkap mulai dari manajemen inventaris, transaksi penjualan, hingga pelaporan keuangan.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost</code>
</aside>
<h3 id="fitur-utama">Fitur Utama</h3>
<ul>
<li><strong>Autentikasi &amp; Manajemen User</strong>: Login berbasis role (Owner, Admin Gudang, Kasir)</li>
<li><strong>Master Data</strong>: Kategori, Supplier, Unit, dan Produk dengan barcode</li>
<li><strong>Transaksi Penjualan</strong>: Proses checkout dengan validasi stok dan pembayaran</li>
<li><strong>Hold Transaksi</strong>: Tahan transaksi tanpa mengurangi stok, bisa checkout nanti</li>
<li><strong>Retur Barang</strong>: Proses retur dengan restorasi stok otomatis</li>
<li><strong>Inventory Management</strong>: Track pergerakan stok, adjustment stok manual</li>
<li><strong>Operational Costs</strong>: Catat biaya operasional (sewa, listrik, dll)</li>
<li><strong>Activity Logs</strong>: Log semua aktivitas penting untuk audit trail</li>
<li><strong>Notifikasi &amp; Alerts</strong>: Alert stok rendah, notifikasi in-app</li>
<li><strong>Laporan Keuangan</strong>: Ringkasan penjualan, dashboard, laporan keuangan</li>
</ul>
<h3 id="authentication">Authentication</h3>
<p>API menggunakan <strong>Laravel Sanctum</strong> untuk autentikasi. Setiap request yang memerlukan autentikasi harus include header:</p>
<pre><code>Authorization: Bearer {token}</code></pre>
<p>Token didapat dari endpoint <code>/api/auth/login</code> setelah berhasil login.</p>
<h3 id="role-based-access-control">Role-Based Access Control</h3>
<p>Akses endpoint diatur berdasarkan role user:</p>
<ul>
<li><strong>Owner</strong>: Akses penuh, bisa create/edit/delete master data dan laporan</li>
<li><strong>Admin Gudang</strong>: Bisa manage produk dan stok</li>
<li><strong>Kasir</strong>: Bisa lakukan transaksi penjualan dan retur</li>
</ul>
<h3 id="response-format">Response Format</h3>
<p>Semua response mengikuti format JSON konsisten:</p>
<pre><code class="language-json">{
  "success": true,
  "message": "Operation successful",
  "data": { /* resource data */ },
  "meta": { /* pagination info jika applicable */ }
}</code></pre>
<h3 id="error-handling">Error Handling</h3>
<p>Jika terjadi error, response akan terlihat seperti:</p>
<pre><code class="language-json">{
  "success": false,
  "message": "Error message here",
  "errors": { /* validation errors jika ada */ }
}</code></pre>
<p>HTTP Status Codes:</p>
<ul>
<li><code>200 OK</code>: Request berhasil</li>
<li><code>201 Created</code>: Resource berhasil dibuat</li>
<li><code>400 Bad Request</code>: Input tidak valid</li>
<li><code>401 Unauthorized</code>: Tidak terautentikasi</li>
<li><code>403 Forbidden</code>: Tidak punya akses</li>
<li><code>404 Not Found</code>: Resource tidak ditemukan</li>
<li><code>500 Internal Server Error</code>: Server error</li>
</ul>
