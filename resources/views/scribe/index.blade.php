        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>API ini menggunakan <strong>Bearer Token Authentication</strong> berbasis Laravel Sanctum.</p>
<h2 id="login">Login</h2>
<p>Untuk mendapatkan token, gunakan endpoint <code>/api/auth/login</code> dengan kredensial user:</p>
<pre><code class="language-json">{
  "username": "admin",
  "password": "password"
}</code></pre>
<p>Response akan berisi token:</p>
<pre><code class="language-json">{
  "success": true,
  "message": "Login successful",
  "data": {
    "user": { /* user data */ },
    "token": "1|abcdef123456..."
  }
}</code></pre>
<h2 id="menggunakan-token">Menggunakan Token</h2>
<p>Setiap request yang memerlukan autentikasi harus include header:</p>
<pre><code>Authorization: Bearer {token}</code></pre>
<p>Contoh menggunakan curl:</p>
<pre><code class="language-bash">curl -H "Authorization: Bearer 1|abcdef123456..." \
     http://localhost/api/auth/me</code></pre>
<h2 id="logout">Logout</h2>
<p>Untuk logout dan invalidate token, gunakan endpoint <code>/api/auth/logout</code> dengan method POST dan token yang sama.</p>

